package com.vmware.vfabric.ide.eclipse.tcserver.internal.ui;

import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 * @author Steffen Pingel
 * @author Christian Dupuis
 */
public class TcServerUiPlugin extends AbstractUIPlugin {

	public static final String PLUGIN_ID = "com.vmware.vfabric.ide.eclipse.tcserver.ui";

	private static TcServerUiPlugin plugin;

	public static TcServerUiPlugin getDefault() {
		return plugin;
	}

	public TcServerUiPlugin() {
	}

	@Override
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	@Override
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

}
