package com.vmware.vfabric.ide.eclipse.tcserver.livegraph;

import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 * 
 * @author Leo Dos Santos
 */
public class TcServerLiveGraphPlugin extends AbstractUIPlugin {

	// The plug-in ID
	public static final String PLUGIN_ID = "com.vmware.vfabric.ide.eclipse.tcserver.livegraph"; //$NON-NLS-1$

	public static String FLAG_LIVE_BEANS = "-Dspring.liveBeansView.mbeanDomain"; //$NON-NLS-1$

	// The shared instance
	private static TcServerLiveGraphPlugin plugin;

	/**
	 * The constructor
	 */
	public TcServerLiveGraphPlugin() {
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.eclipse.ui.plugin.AbstractUIPlugin#start(org.osgi.framework.BundleContext
	 * )
	 */
	@Override
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.eclipse.ui.plugin.AbstractUIPlugin#stop(org.osgi.framework.BundleContext
	 * )
	 */
	@Override
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	/**
	 * Returns the shared instance
	 * 
	 * @return the shared instance
	 */
	public static TcServerLiveGraphPlugin getDefault() {
		return plugin;
	}

}
